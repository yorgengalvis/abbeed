    public void reestablecerCampos(){
        ncedula.setText("");
        txtname.setText("");
        ntelefono.setText("");
        cantLlevar.setText("");
        txtvalorUnitario.setText("");
        txtvalortotal.setText("");
    }
    public EnumProducto validarNTipo(String ntipo){
        switch(ntipo){
            case "1":
                return EnumProducto.Papeleria;
            case "2":
                return EnumProducto.Supermercado;
            case "3": 
                return EnumProducto.Drogueria;
            default:
                return EnumProducto.Papeleria;
        }  
    }
    
    private void establecerProducto(String codigo) {
        Producto pro = this.controlador.buscarProducto(codigo);
        pname.setText(pro.getNombre());
        ptipo.setText(" " + pro.getTipo());
        pvaloru.setText("" + pro.getPrecioUnidad());
        pstock.setText("" + pro.getCantidad());
        pstockminimo.setText("" + pro.getStockMinimo());
        txtiva.setText(pro.getTipo().getIva()+"%");

    }

