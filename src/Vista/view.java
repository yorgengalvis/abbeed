    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int cedula = Integer.parseInt(JOptionPane.showInputDialog("Digite el numero de cedula"));
        String name = JOptionPane.showInputDialog("Digite los nombres del cliente");
        String telefono = JOptionPane.showInputDialog("Digite el telefono del cliente");
        if (this.controlador.guardarCliente(name, cedula, telefono)) {
            JOptionPane.showMessageDialog(null, "Cliente agregado con éxito.");
        } else {
            JOptionPane.showMessageDialog(null, "El cliente no ha sido agregado, ya existe.");
        }

    }//GEN-LAST:event_jButton2ActionPerformed

